#include "libft.h"

double	ft_atof(const char *s)
{
	double	a;
    float	i;

	a = 0.0;
    i = 10;
	while (*s != '\0' && ft_isdigit(*s))
	{
		a = a * 10.0 + (*s - '0');
		s++;
	}
	if (*s == '.')
	{
		s++;
		while (*s != '\0' && ft_isdigit(*s))
		{
			a += (*s - '0') / i;
            i *= 10;
			s++;
		}
	}
	return (a);
}
