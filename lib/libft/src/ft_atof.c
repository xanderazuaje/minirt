#include "libft.h"

double	ft_atof(const char *s)
{
	double	a;
	int		e;
	int		c;

	a = 0.0;
	e = 0;
	while ((c = *s++) != '\0' && ft_isdigit(c)) {
		a = a*10.0 + (c - '0');
	}
	if (c == '.') {
		while ((c = *s++) != '\0' && ft_isdigit(c)) {
			a = a*10.0 + (c - '0');
			e = e-1;
		}
	}
	while (e < 0) {
		a *= 0.1;
		e++;
	}
	return a;
}